package com.anthunt.poi.mapper.model;

public class DAOParams {

	private int excelIdx;
	private int sheetIdx;
	private int columnIdx;
	
	public int getExcelIdx() {
		return excelIdx;
	}
	
	public void setExcelIdx(int excelIdx) {
		this.excelIdx = excelIdx;
	}
	
	public int getSheetIdx() {
		return sheetIdx;
	}
	
	public void setSheetIdx(int sheetIdx) {
		this.sheetIdx = sheetIdx;
	}
	
	public int getColumnIdx() {
		return columnIdx;
	}
	
	public void setColumnIdx(int columnIdx) {
		this.columnIdx = columnIdx;
	}
	
}
